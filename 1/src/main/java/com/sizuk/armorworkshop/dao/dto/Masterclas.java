package com.sizuk.armorworkshop.dao.dto;

public class Masterclas {

    private int id;
    private String masterclassName;
    private String masterclassText;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMasterclassName() {
        return masterclassName;
    }

    public void setMasterclassName(String masterclassName) {
        this.masterclassName = masterclassName;
    }

    public String getMasterclassText() {
        return masterclassText;
    }

    public void setMasterclassText(String masterclassText) {
        this.masterclassText = masterclassText;
    }
}
